                            <p class="text-extra-dark-gray text-medium" style="font-size: 18px; line-height: 2em;">R Squared, Adjusted R Squared, and Root Mean Squared Error (RMSE) are commonly used metrics to evaluate the performance of a linear regression model. Let's take a look at each of these metrics in detail:

                                <ol>

                                   <li class="text-extra-dark-gray text-medium" style="font-size: 18px; line-height: 2em; font-weight: 600"><p style="font-weight: 400"><strong class="text-extra-dark-gray text-medium">R Squared:</strong> R Squared (also known as the coefficient of determination) is a measure of how well the regression model fits the data. <br>It represents the proportion of the variance in the dependent variable that is explained by the independent variables. R Squared ranges from 0 to 1, with a higher value indicating a better fit of the model to the data.</p></li>



                                    <p class="text-extra-dark-gray text-medium" style="font-size: 18px; line-height: 2em;"><strong class="text-extra-dark-gray text-medium">Example:</strong> Suppose we have a linear regression model that predicts the price of a house based on its square footage. The R Squared value of this model is 0.8, which means that 80% of the variance in the house prices can be explained by the square footage.</p>



                                    

                                    <li class="text-extra-dark-gray text-medium" style="font-size: 18px; line-height: 2em; font-weight: 600"><p style="font-weight: 400"><strong class="text-extra-dark-gray text-medium">Adjusted R Squared:</strong> The R Squared value may increase even if a new variable is added to the model, even if it has no effect on the dependent variable. The adjusted R Squared takes into account the number of independent variables in the model and adjusts the R Squared value accordingly. It is a better indicator of the model's fit to the data compared to R Squared.</p></li>



                                    <p class="text-extra-dark-gray text-medium" style="font-size: 18px; line-height: 2em;"><strong class="text-extra-dark-gray text-medium">Example:</strong> Suppose we add a new variable "number of bedrooms" to our previous example of a linear regression model that predicts the price of a house based on its square footage. The R Squared value of this new model may increase, but the adjusted R Squared may decrease, indicating that the new variable may not have a significant effect on the model's performance.</p>



                                    

                                    <li class="text-extra-dark-gray text-medium" style="font-size: 18px; line-height: 2em; font-weight: 600"><p style="font-weight: 400"><strong class="text-extra-dark-gray text-medium">Root Mean Squared Error (RMSE): </strong> RMSE is a measure of the difference between the actual values and the predicted values. It represents the average error in the predictions made by the model. The lower the RMSE, the better the model is at making predictions.</p></li>

                                    

                                    <p class="text-extra-dark-gray text-medium" style="font-size: 18px; line-height: 2em;"><strong class="text-extra-dark-gray text-medium">Example:</strong> Suppose we have a linear regression model that predicts the sales of a product based on its advertising budget. The RMSE of this model is $1000, which means that on average, the predictions made by the model deviate from the actual sales by $1000.</p>

                                </ol>

                            </p>

                                

