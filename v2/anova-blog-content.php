<i>library(stats)

# Load the dataset
data <- read.csv("housing_data.csv")

# Fit a linear regression model
model <- lm(price ~ sqft + bedrooms, data = data)

# Print the model summary
summary(model)</i>
</p>

                            <p class="text-extra-dark-gray text-medium" style="font-size: 18px; line-height: 2em;"><strong>Python code:</strong>
<i>import pandas as pd
from sklearn.linear_model import LinearRegression

# Load the dataset
data = pd.read_csv("housing_data.csv")

# Prepare the data
X = data[['sqft', 'bedrooms']]
y = data['price']

# Create and fit the linear regression model
model = LinearRegression()
model.fit(X, y)

# Print the coefficients
print("Intercept:", model.intercept_)
print("Coefficients:", model.coef_)</i>
</p>

